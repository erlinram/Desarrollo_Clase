// - Desarrolle un programa que permita a través de funciones, desarrollar la suma, resta, 
// multiplicación y división de dos números 
//Suma
let var1 = 500;
let var2 = 10;
function calcular(var1,var2){
    let suma = var1 + var2;
    return suma;

}
console.log(calcular(500,10));


//Resta
let var3 = 30;
let var4 = 2;
function respuesta(var3,var4){
    let resta = var3 - var4;
    return resta;

}
console.log(respuesta(30,2));

//Multiplicación
let var5 = 3;
let var6 = 5;
function resultado(var5,var6){
    let multiplicacion = var5 * var6;
    return multiplicacion;

}
console.log(resultado(3,5))

//División 
let var7 = 15;
let var8 = 3;
function final(var7,var8){
    let division = var7 / var8;
    return division;

}
